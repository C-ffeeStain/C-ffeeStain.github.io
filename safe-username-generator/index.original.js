function getRandomFromArray(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

function displayNum(num) {
    switch (num) {
        case num < 10:
            return "000" + num;
        case num < 100:
            return "00" + num;
        case num < 1000:
            return "0" + num;
        default:
            console.log("returning default")
            return num;
    }
}

function generateUsername() {
    var noun = getRandomFromArray(nouns);
    var adjective = getRandomFromArray(adjectives);
    var username = adjective + noun + displayNum(Math.floor(Math.random() * 1000));
    document.getElementById("username").innerHTML = username;
}